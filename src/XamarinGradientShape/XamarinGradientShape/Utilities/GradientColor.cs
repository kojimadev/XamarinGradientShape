namespace XamarinGradientShape.Utilities
{
	/// <summary>
	/// グラデーションの色のタイプ
	/// </summary>
	public enum GradientColor
	{
		/// <summary>
		/// 透明色
		/// </summary>
		Transparent,

		/// <summary>
		/// 線形 濃い赤グラデーション
		/// </summary>
		LinerDarkRed,

		/// <summary>
		/// 線形 明るい青グラデーション
		/// </summary>
		LinerLightBlue,

		/// <summary>
		/// 線形 濃い黄色グラデーション
		/// </summary>
		LinerDarkYellow,

		/// <summary>
		/// 線形 黒グラデーション
		/// </summary>
		LinerBlack,

		/// <summary>
		/// 線形 灰色グラデーション
		/// </summary>
		LinerGray,

		/// <summary>
		/// 放射状 赤グラデーション
		/// </summary>
		RadialRedBrush,

		/// <summary>
		/// 放射状 青グラデーション
		/// </summary>
		RadialBlueBrush,

		/// <summary>
		/// 放射状 黄グラデーション
		/// </summary>
		RadialYellowBrush,

	}
}
